const treeCounter = () => {
    let trees = 0;
    let rowLength = input[0].length;
    col = 0;
    for(let row = 0; row < input.length; row++) {
        if(input[row].charAt(col) === "#")
            trees++;
        col = (col + 3) % rowLength; 
    }
    console.log(`... and the answer to part 1 is ${trees}`);
}

treeCounter();

const treeCounterPart2 = (slope) => {
    let trees = 0;
    let rowLength = input[0].length;
    let col = 0;
    let row = 0;
    while(row < input.length) 
    {
        if(input[row].charAt(col) === "#")
            trees++;

        col = (col + slope.right) % rowLength; 
        row += slope.down;
    }

    slope.result = trees;

    console.log(`... trees for slope right: ${slope.right}, down: ${slope.down} is ${trees}`);
}

const slopes = [
    {
        right: 1,
        down: 1,
        result: 0
    },
    {
        right: 3,
        down: 1,
        result: 0
    },
    {
        right: 5,
        down: 1,
        result: 0
    },
    {
        right: 7,
        down: 1,
        result: 0
    },
    {
        right: 1,
        down: 2,
        result: 0
    },
]
for(let slope of slopes)
    treeCounterPart2( slope );

const result = slopes.map(s => s.result).reduce( (previous, current) => current * ( previous || 1 ) );
console.log(`... and the answer to part 2 is ${result}`);
